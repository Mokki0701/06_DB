-- 1번
SELECT  STUDENT_NO 학번, STUDENT_NAME 이름, ENTRANCE_DATE 입학년도
FROM TB_STUDENT
WHERE DEPARTMENT_NO = '002'
ORDER BY 입학년도;

-- 2번
SELECT PROFESSOR_NAME , PROFESSOR_SSN N
FROM TB_PROFESSOR
WHERE LENGTH(PROFESSOR_NAME) <> 3;

-- 3번
SELECT PROFESSOR_NAME 교수이름 , 
				FLOOR(MONTHS_BETWEEN(SYSDATE,TO_DATE('19'||SUBSTR(PROFESSOR_SSN,1,6),'YYYYMMDD') ) /12 ) 나이
FROM TB_PROFESSOR
WHERE SUBSTR(PROFESSOR_SSN,8,1) = '1' 
ORDER BY 나이;

-- 4번
SELECT SUBSTR(PROFESSOR_NAME,2,LENGTH(PROFESSOR_NAME)-1) 이름 -- 길이 지정 안하면 자동으로 끝까지 출력됌
FROM TB_PROFESSOR;

-- 5번
SELECT STUDENT_NO , STUDENT_NAME 
FROM TB_STUDENT
WHERE (TO_NUMBER(TO_CHAR(ENTRANCE_DATE,'YYYY')) - TO_NUMBER('19'||SUBSTR(STUDENT_SSN,1,2) ,'9999')) > 19; 

-- 6번
SELECT STUDENT_NO , STUDENT_NAME 
FROM TB_STUDENT
WHERE SUBSTR(STUDENT_NO,1,1) <> 'A'; 

-- 7번
SELECT ROUND( SUM(POINT)/COUNT(POINT),1)
FROM TB_GRADE
WHERE STUDENT_NO = 'A517178';






























































